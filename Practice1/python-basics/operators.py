# 1. Arithmetic operators
a = 10
b = 3
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b)

# 2. Comparison operators
x = 5
y = 10
print("x == y:", x == y)
print("x != y:", x != y)
print("x > y:", x > y)
print("x < y:", x < y)
print("x >= y:", x >= y)
print("x <= y:", x <= y)

# 3. Assignment operators
z = 10
z += 5
print("z += 5:", z)
z -= 3
print("z -= 3:", z)
z *= 2
print("z *= 2:", z)
z /= 4
print("z /= 4:", z)

# 4. Logical operators
a = True
b = False
print("a and b:", a and b)
print("a or b:", a or b)
print("not a:", not a)

# 5. Membership operators
fruits = ["apple", "banana", "cherry"]
print("Is 'apple' in fruits?", "apple" in fruits)
print("Is 'orange' not in fruits?", "orange" not in fruits)
