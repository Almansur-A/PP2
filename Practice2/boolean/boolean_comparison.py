# Example 1: Equal operator
print(5 == 5)
print(5 == 3)

# Example 2: Not equal operator
print(5 != 3)
print(5 != 5)

# Example 3: Greater than / less than
print(10 > 5)
print(2 < 4)

# Example 4: Greater than or equal / less than or equal
print(5 >= 5)
print(4 <= 2)

# Example 5: Comparing strings
print("Python" == "python")
print("abc" < "bcd")
