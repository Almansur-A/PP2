# Example 1: Boolean values
a = True
b = False
print(a, b)

# Example 2: Boolean from comparisons
print(10 > 5)
print(3 == 3)

# Example 3: Using bool() function
print(bool(0))
print(bool(1))
print(bool("Python"))

# Example 4: Negation
x = True
print(not x)

# Example 5: Boolean in arithmetic
print(True + True)   # 2
print(False + 5)     # 5
