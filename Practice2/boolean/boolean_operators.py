# Example 1: And operator
print(True and False)
print(True and True)

# Example 2: Or operator
print(True or False)
print(False or False)

# Example 3: Not operator
print(not True)
print(not False)

# Example 4: Combine operators
print((5 > 3) and (2 < 4))
print((5 < 3) or (2 > 1))

# Example 5: Boolean in if statement
if 5 > 2 and 3 < 5:
    print("Both conditions are True")
