# Example 1: One-line if
x = 10
if x > 5: print("x > 5")

# Example 2: One-line if-else
y = 2
print("Even") if y % 2 == 0 else print("Odd")

# Example 3: Short-hand with strings
name = "Bob"
print("Hello Bob") if name == "Bob" else print("Hello stranger")

# Example 4: Assign using shorthand
age = 20
status = "Adult" if age >= 18 else "Minor"
print(status)

# Example 5: Inline with calculation
a, b = 5, 10
print(a + b) if a > 0 else print(a - b)
