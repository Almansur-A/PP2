# Example 1: Basic if-else
x = 7
if x > 5:
    print("x > 5")
else:
    print("x <= 5")

# Example 2: Check negative
n = -3
if n >= 0:
    print("Positive")
else:
    print("Negative")

# Example 3: Even/Odd
num = 4
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

# Example 4: Temperature check
temp = 25
if temp > 30:
    print("Hot")
else:
    print("Comfortable")

# Example 5: String comparison
name = "Alice"
if name == "Alice":
    print("Hello Alice")
else:
    print("Hello stranger")
