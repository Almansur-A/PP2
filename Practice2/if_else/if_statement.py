# Example 1: Simple if
x = 10
if x > 5:
    print("x is greater than 5")

# Example 2: If with variable
y = 3
if y < 5:
    print("y is less than 5")

# Example 3: Multiple ifs
a = 5
b = 10
if a < b:
    print("a is smaller than b")

# Example 4: Nested if
num = 7
if num > 0:
    if num < 10:
        print("num is a single-digit positive number")

# Example 5: Using input
n = int(input("Enter a number: "))
if n % 2 == 0:
    print("Even number")
