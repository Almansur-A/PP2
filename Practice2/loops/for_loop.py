# Example 1: Simple for loop
for i in range(5):
    print(i)

# Example 2: Sum using for
total = 0
for i in range(1, 6):
    total += i
print(total)

# Example 3: Iterate list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example 4: Range with step
for i in range(2, 10, 2):
    print(i)

# Example 5: String iteration
s = "Python"
for c in s:
    print(c)
